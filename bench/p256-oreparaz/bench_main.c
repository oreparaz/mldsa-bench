/* bench_main.c — measure ec_p256_verify on a single hard-coded
 * (msg, sig, pk) tuple.
 *
 * oreparaz/p256 takes the raw message and computes SHA-256 internally
 * (unlike p256-m which expects a pre-computed hash). The bench thus
 * includes SHA-256("hi\n") in the verify cost — ~3,500 instructions
 * for a single SHA-256 block, negligible vs the ECDSA cost (millions).
 *
 * Vectors are the ones from oreparaz/p256/demo.c (verifies "hi\n").
 */
#include <stdint.h>
#include <stddef.h>
#include <string.h>

#include "bench_io.h"
#include "p256.h"

#ifndef BENCH_ITERS
#  define BENCH_ITERS 1u
#endif

/* "hi\n" -- the message that was signed in oreparaz/p256/demo.c */
static const uint8_t msg[] = { 'h', 'i', '\n' };

static const uint8_t sig[64] = {
    /* r */
    0x6c,0x98,0xb6,0x80,0x9f,0x6e,0x2c,0x73,
    0x95,0xc6,0xc9,0xf1,0x8a,0x30,0x28,0x21,
    0xc5,0xf6,0x03,0x69,0xd3,0xab,0xd1,0x92,
    0xe9,0xe5,0xc4,0xf6,0x07,0xd5,0x18,0xd3,
    /* s */
    0x4a,0x9d,0x74,0xa0,0xf4,0x4c,0x61,0x03,
    0x13,0x30,0xa7,0xe3,0xf2,0x79,0x08,0xf5,
    0xc5,0x89,0xfe,0x64,0x27,0xdb,0x7c,0x3f,
    0x3f,0x74,0x09,0x55,0x9e,0x50,0x0c,0x3c
};
static const uint8_t pk[65] = {
    0x04,
    /* x */
    0x56,0x16,0xab,0x0d,0xf8,0x5a,0xc8,0x9c,
    0xc8,0x53,0xb8,0x4e,0x53,0xca,0xb5,0x35,
    0x22,0x4a,0x7d,0xbc,0x39,0x27,0x02,0x76,
    0xdd,0xa8,0x00,0x85,0x3e,0xe8,0xae,0x9b,
    /* y */
    0x68,0xb9,0x53,0x59,0x70,0x4f,0x87,0xe0,
    0x23,0x42,0x4d,0x5d,0x84,0x2f,0x08,0x21,
    0xd8,0x8c,0xe0,0x1f,0xb6,0xa8,0x1a,0x6a,
    0x1c,0x87,0x8a,0x81,0x13,0x0c,0x61,0x68
};

static volatile int g_acc;

int main(void) {
    int rc = 0;
    for (unsigned i = 0; i < BENCH_ITERS; i++) {
        /* p256_verify mutates its msg pointer (sigh) so re-copy each iter.
         * The copy is 3 bytes and adds ~6 instructions/iter — much less
         * than ECDSA verify's ~10M-30M. */
        uint8_t local[sizeof msg];
        memcpy(local, msg, sizeof msg);
        p256_ret_t r = p256_verify(local, sizeof local, (uint8_t *)sig, pk);
        g_acc ^= (int)r;
        if (r != P256_SUCCESS) rc = 1;
    }
    uint8_t out[3];
    out[0] = (uint8_t)0xb2;                 /* tag for p256-oreparaz */
    out[1] = (uint8_t)(BENCH_ITERS & 0xff);
    out[2] = (uint8_t)(g_acc & 0xff);
    bench_emit_hex(out, 3);
    return rc;
}
