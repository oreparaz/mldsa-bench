/* bench_main.c — measure p256_ecdsa_verify on a single hard-coded
 * (pub, sig, hash) tuple from RFC 6979 A.2.5 (NIST P-256 + SHA-256).
 *
 * These are the same vectors p256-m's own test-data.h ships, so the
 * bench reproduces the exact verify path that the upstream test suite
 * exercises against an x86 build.
 */
#include <stdint.h>
#include <stddef.h>

#include "bench_io.h"
#include "p256-m.h"

#ifndef BENCH_ITERS
#  define BENCH_ITERS 1u
#endif

/* RFC 6979 A.2.5 (P-256 + SHA-256) public key (x||y, big-endian) */
static const uint8_t ecdsa_pub[64] = {
    0x60, 0xfe, 0xd4, 0xba, 0x25, 0x5a, 0x9d, 0x31,
    0xc9, 0x61, 0xeb, 0x74, 0xc6, 0x35, 0x6d, 0x68,
    0xc0, 0x49, 0xb8, 0x92, 0x3b, 0x61, 0xfa, 0x6c,
    0xe6, 0x69, 0x62, 0x2e, 0x60, 0xf2, 0x9f, 0xb6,
    0x79, 0x03, 0xfe, 0x10, 0x08, 0xb8, 0xbc, 0x99,
    0xa4, 0x1a, 0xe9, 0xe9, 0x56, 0x28, 0xbc, 0x64,
    0xf2, 0xf1, 0xb2, 0x0c, 0x2d, 0x7e, 0x9f, 0x51,
    0x77, 0xa3, 0xc2, 0x94, 0xd4, 0x46, 0x22, 0x99,
};
/* SHA-256("sample") */
static const uint8_t h256a[32] = {
    0xaf, 0x2b, 0xdb, 0xe1, 0xaa, 0x9b, 0x6e, 0xc1,
    0xe2, 0xad, 0xe1, 0xd6, 0x94, 0xf4, 0x1f, 0xc7,
    0x1a, 0x83, 0x1d, 0x02, 0x68, 0xe9, 0x89, 0x15,
    0x62, 0x11, 0x3d, 0x8a, 0x62, 0xad, 0xd1, 0xbf,
};
/* RFC 6979 ECDSA signature on h256a */
static const uint8_t sig256a[64] = {
    0xef, 0xd4, 0x8b, 0x2a, 0xac, 0xb6, 0xa8, 0xfd,
    0x11, 0x40, 0xdd, 0x9c, 0xd4, 0x5e, 0x81, 0xd6,
    0x9d, 0x2c, 0x87, 0x7b, 0x56, 0xaa, 0xf9, 0x91,
    0xc3, 0x4d, 0x0e, 0xa8, 0x4e, 0xaf, 0x37, 0x16,
    0xf7, 0xcb, 0x1c, 0x94, 0x2d, 0x65, 0x7c, 0x41,
    0xd4, 0x36, 0xc7, 0xa1, 0xb6, 0xe2, 0x9f, 0x65,
    0xf3, 0xe9, 0x00, 0xdb, 0xb9, 0xaf, 0xf4, 0x06,
    0x4d, 0xc4, 0xab, 0x2f, 0x84, 0x3a, 0xcd, 0xa8,
};

/* p256-m's interface declares this extern; we never sign or generate keys
 * here so it's never called, but we still need to satisfy the linker. */
int p256_generate_random(uint8_t *output, unsigned output_size) {
    for (unsigned i = 0; i < output_size; i++) output[i] = 0;
    return 0;
}

static volatile int g_acc;

int main(void) {
    int rc = 0;
    for (unsigned i = 0; i < BENCH_ITERS; i++) {
        int r = p256_ecdsa_verify(sig256a, ecdsa_pub, h256a, sizeof h256a);
        g_acc ^= r;
        if (r != 0) rc = 1;
    }
    uint8_t out[3];
    out[0] = (uint8_t)0xa1;                  /* tag for p256-m */
    out[1] = (uint8_t)(BENCH_ITERS & 0xff);
    out[2] = (uint8_t)(g_acc & 0xff);
    bench_emit_hex(out, 3);
    return rc;
}
